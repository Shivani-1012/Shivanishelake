package datatypes;

public class BooleanExample {

	public static void main(String[] args) {
		
		// I want to store my result-->pass/fail
		//pass-->true, fail-->false
		
		boolean result; //variable declration
		
		result = false;  //variable initialization
		
		System.out.println(result); //variable usage
		
		System.out.println("My passing status is " + result); //variable usage
		
		//I want to store switch condition-->ON/OFF
		//ON-->true, OFF-->false
		
		boolean switchCondition; //variable declaration
		
		switchCondition = true;  //variable initialization
		
		System.out.println("Switch condition is " + switchCondition);
		
		System.out.println(switchCondition);
		
		
		

	}

}
