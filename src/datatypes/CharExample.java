package datatypes;

public class CharExample {

	public static void main(String[] args) {
		
		// I want to store my 10th division
		
		char division; //variable declaration
		
		division = 'A'; //variable initialization
		
		System.out.println(division); //variable usage
		
		System.out.println("My div is "+division);
		
		//I want to store my gender-->M,F, male,female
		
		char gender; //variable declaration
		
		gender = 'F'; //variable initialization
		
		System.out.println(gender); //variable usage
		
		System.out.println("My gender is " +gender);
		

	}

}
